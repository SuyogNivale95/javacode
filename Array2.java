package Array;

public class Array2 
{
	public static void main(String[] args) 
	{
		int rollno[]=new int[3];
		rollno[0]=11;
		rollno[1]=22;
		rollno[2]=33;
		
		String name[]=new String[3];
		name[0]="Suyog";
		name[1]="Mahesh";
		name[2]="Priyanka";
		
		for(int i=0;i<3;i++)
		{
			System.out.println(rollno[i]);
			System.out.println(name[i]);
		}
	}
}
